

DROP FUNCTION IF EXISTS horaOk;

DELIMITER $$

CREATE FUNCTION horaOk(h INT,m INT,s INT)RETURNS BOOLEAN

BEGIN 
	
	IF(h>23) THEN
	  RETURN FALSE;
	 ELSEIF(m>59) THEN
			RETURN FALSE;
	 ELSEIF(s>59) THEN
			RETURN FALSE;
	ELSE
		RETURN TRUE;	
	END IF;
				

END $$

DELIMITER ;

SELECT horaOK(22,51,53);
SELECT horaOK(00,00,00);
SELECT horaOK(23,59,59);
SELECT horaOK(22,51,60);
SELECT horaOK(22,60,53);
SELECT horaOK(24,51,53);